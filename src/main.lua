function new_animation(img, framew, frameh, dur)
	local animation = {}
	animation.spriteSheet = img
	animation.quads = {}

	for y = 0, img:getHeight() - frameh, frameh do
		for x = 0, img:getWidth() - framew, framew do
			table.insert(animation.quads, love.graphics.newQuad(x, y, framew, frameh, img:getDimensions()))
		end
	end

	animation.duration = dur or 1
	animation.currentTime = 0

	return animation
end

function love.update(dt)

	if love.keyboard.isDown("left") then
		player.x = player.x - player.spd * dt
		if not love.keyboard.isDown('right') then
			player.wants_left = true
			player.wants_right = false
		end
		player.is_walking = true
	end
	if love.keyboard.isDown("up") then
		player.y = player.y - player.spd * dt
		player.is_walking = true
	end
	if love.keyboard.isDown("down") then
		player.y = player.y + player.spd * dt
		player.is_walking = true
	end
	if love.keyboard.isDown("right") then
		player.x = player.x + player.spd * dt
		if not love.keyboard.isDown('left') then
			player.wants_right = true
			player.wants_left = false
		end
		player.is_walking = true
	end

	if not love.keyboard.isDown('left') and
	not love.keyboard.isDown('right') and
	not love.keyboard.isDown('down') and
	not love.keyboard.isDown('up') then
		player.is_walking = false
	end
	
	if love.keyboard.isDown('left') and love.keyboard.isDown('right') then
		player.is_walking = false
	end

	player.anim.currentTime = player.anim.currentTime + dt
	if player.anim.currentTime >= player.anim.duration then
		player.anim.currentTime = player.anim.currentTime - player.anim.duration
	end
end

