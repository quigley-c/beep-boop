function camera_set()
	love.graphics.push()
	love.graphics.rotate(camera.rotation)
	love.graphics.scale(1 / camera.zoom, 1 / camera.zoom)
	love.graphics.translate(camera.x, camera.y)
end

function camera_unset()
	love.graphics.pop()
end

function love.draw()

	for rowIndex = 1, #tiletable do
		local row = tiletable[rowIndex]
		for columnIndex = 1, #row do
			local number = row[columnIndex]
			love.graphics.draw(tileset, quads[number], (columnIndex-1)*tileW, (rowIndex-1)*tileH)
		end
	end


	camera_set()

	if player.wants_left then
		if player.is_walking then
			player.anim = spr_walk_left
		else
			player.anim = spr_left
		end
	elseif player.wants_right then
		if player.is_walking then
			player.anim = spr_walk_right
		else
			player.anim = spr_right
		end
	end

	love.graphics.print(
	"player wants left:" .. tostring(player.wants_left) ..
	"\tplayer wants right:" .. tostring(player.wants_right), 50, 5)

	local cur_frame = math.floor(player.anim.currentTime / player.anim.duration * #player.anim.quads) + 1
	love.graphics.draw(player.anim.spriteSheet, player.anim.quads[cur_frame],  player.x, player.y)
	camera_unset()
end
